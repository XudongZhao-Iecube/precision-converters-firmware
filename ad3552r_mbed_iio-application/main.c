/* @brief	Main function
 * @details	This is a main entry function for AD3552R IIO application
 */
int main(void)
{
	/* Initialize the AD3552R IIO interface */
	if (ad3552r_iio_initialize() != SUCCESS) {
		assert(false);
	}

	while (1) {
		/* Monitor the IIO client events */
		ad3552r_iio_event_handler();
	}
}
